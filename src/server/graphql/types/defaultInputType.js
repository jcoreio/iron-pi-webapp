// @flow

import type {Model} from 'sequelize'
import * as graphql from 'graphql'
import {attributeFields} from 'graphql-sequelize'

type Options = {
  cache?: Object,
  fields?: {
    [name: string]: graphql.GraphQLInputFieldConfig,
  },
}

export default function defaultInputType(
  model: Class<Model<any>>,
  options?: Options = {}
): graphql.GraphQLInputObjectType {
  const {fields = {}, ...attributeFieldsOptions} = options
  return new graphql.GraphQLInputObjectType({
    name: `Input${model.options.name.singular}`,
    fields: () => ({
      ...attributeFields(model, {
        allowNull: true,
        ...attributeFieldsOptions,
        exclude: (key: string) => {
          if (fields[key] || model.attributes[key]._autoGenerated) {
            return true
          }
        },
      }),
      ...fields,
    })
  })
}

